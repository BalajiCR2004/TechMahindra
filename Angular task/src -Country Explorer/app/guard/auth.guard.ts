import { Injectable } from '@angular/core';
import { CanActivate, Router } from '@angular/router';

@Injectable({
  providedIn: 'root'
})
export class AuthGuard implements CanActivate {
  constructor(private router: Router) {}

  canActivate(): boolean {
    const isLoggedIn = localStorage.getItem('loggedIn') === 'true'; // âœ… Ensure it checks against "true"
    
    if (isLoggedIn) {
      return true; // âœ… Allow access if logged in
    } else {
      this.router.navigate(['/login']); // ðŸš« Redirect to login if not logged in
      return false;
    }
  }
}
